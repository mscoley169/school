public class ModulusExample {
    public static void main(String[] args) {
        System.out.println("Java Modulus Operator example");
        System.out.println("--------------------------------------------");
        /*
         * Modulus operator returns reminder of the devision of
         * floating point or integer types.
         */
         
         int i = 50;
         double d = 32;
         System.out.println("The result from the modulus operator is the "
         		+ "remainder after dividing two numbers" );
         
         System.out.println("If integer i = 10 then i modulus 10 = " + i%10);
         System.out.println("If double d = 32.0 then d modulus 10 = " + d%10);
    }
}
