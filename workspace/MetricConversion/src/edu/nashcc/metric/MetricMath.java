package edu.nashcc.metric;

public class MetricMath {

	
	float centimeters;
	float liters;
	float inches;
	float gallons;
		
	public MetricMath (float inches, float gallons) {
		this.inches = inches;
		this.gallons = gallons;
	
	}
	
	public float calcInToCM (float inches){
		centimeters = inches * 2.54F;
		return centimeters;
	}
	public float calcGalToLiter (float gallons){
		liters = gallons * 3.7854F;
		return liters;
	}
}
